package com.example.android.easy_book;

public class MenuImages {
    String ImageMenu1;
    String ImageMenu2;
    String ImageMenu3;

    public MenuImages(String imageMenu1, String imageMenu2, String imageMenu3) {
        ImageMenu1 = imageMenu1;
        ImageMenu2 = imageMenu2;
        ImageMenu3 = imageMenu3;
    }



    public String getImageMenu1() {
        return ImageMenu1;
    }

    public void setImageMenu1(String imageMenu1) {
        ImageMenu1 = imageMenu1;
    }

    public String getImageMenu2() {
        return ImageMenu2;
    }

    public void setImageMenu2(String imageMenu2) {
        ImageMenu2 = imageMenu2;
    }

    public String getImageMenu3() {
        return ImageMenu3;
    }

    public void setImageMenu3(String imageMenu3) {
        ImageMenu3 = imageMenu3;
    }

}
